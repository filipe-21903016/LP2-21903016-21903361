package pt.ulusofona.lp2.theWalkingDEISIGame;

public class SafeHaven {
    int posX;
    int posY;

    public SafeHaven(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }
}
